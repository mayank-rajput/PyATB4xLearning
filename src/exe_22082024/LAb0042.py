#   Loops
#   For Loop
# for variable_name in range(start, Stop, Step) ---------Syntax
# code that has to  be executed

for i in range(1, 10):
    print("Hello World ->", i)

print("\n---------\n")
for i in range(1, 10, 2):
    print("Hello World", i, end=" | ")

print("\n\n---------\n")

for i in range(10, 2, -2):
    print(i)

for i in range(0, -10, -2):
    print(i)
